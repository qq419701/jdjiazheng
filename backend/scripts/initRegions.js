/**
 * 初始化地区数据
 * 检查表中是否已有数据，有则跳过，没有则批量插入
 */
async function 初始化地区数据() {
  try {
    // 连接数据库
    await 数据库连接.authenticate();
    console.log('数据库连接成功');

    // 同步模型（如果表不存在则创建）
    await Region.sync({ alter: true });
    console.log('regions 表同步完成');

    // 检查表中是否已有数据
    const 现有数量 = await Region.count();
    if (现有数量 > 0) {
      console.log(`regions 表已有 ${现有数量} 条数据，跳过初始化`);
      process.exit(0);
      return;
    }

    console.log('开始插入省市区数据...');
    let 省计数 = 0;
    let 市计数 = 0;
    let 区计数 = 0;

    // 逐省插入数据
    for (let 省排序 = 0; 省排序 < 去重后数据.length; 省排序++) {
      const 省数据 = 去重后数据[省排序];

      // 插入省级
      const 省记录 = await Region.create({
        name: 省数据.省,
        parent_id: 0,
        level: 1,
        is_enabled: 1,
        sort: 省排序 + 1,
      });
      省计数++;

      // 插入市级
      for (let 市排序 = 0; 市排序 < 省数据.市.length; 市排序++) {
        const 市数据 = 省数据.市[市排序];

        const 市记录 = await Region.create({
          name: 市数据.市,
          parent_id: 省记录.id,
          level: 2,
          is_enabled: 1,
          sort: 市排序 + 1,
        });
        市计数++;

        // 插入区县级
        for (let 区排序 = 0; 区排序 < 市数据.区.length; 区排序++) {
          await Region.create({
            name: 市数据.区[区排序],
            parent_id: 市记录.id,
            level: 3,
            is_enabled: 1,
            sort: 区排序 + 1,
          });
          区计数++;
        }
      }

      console.log(`已处理省份：${省数据.省}`);
    }

    console.log(`\n初始化完成！`);
    console.log(`省级：${省计数} 条`);
    console.log(`市级：${市计数} 条`);
    console.log(`区县级：${区计数} 条`);
    console.log(`合计：${省计数 + 市计数 + 区计数} 条`);

    process.exit(0);
  } catch (错误) {
    console.error('初始化地区数据出错:', 错误);
    process.exit(1);
  }
}

// 执行初始化
初始化地区数据();
