// ===== 初始化函数 =====
async function 初始化街道数据() {
  try {
    await 数据库连接.authenticate();
    console.log('数据库连接成功');

    await Region.sync({ alter: true });
    console.log('regions 表同步完成');

    let 总计 = 0;
    let 跳过 = 0;
    let 未找到 = 0;

    for (const [省名, 省数据] of Object.entries(街道数据)) {
      // 查找省级记录
      const 省记录 = await Region.findOne({ where: { name: 省名, level: 1 } });
      if (!省记录) {
        console.log(`⚠️  未找到省份：${省名}，跳过`);
        未找到++;
        continue;
      }

      for (const [市名, 市数据] of Object.entries(省数据)) {
        // 查找市级记录（直辖市省市同名）
        const 市记录 = await Region.findOne({ where: { name: 市名, parent_id: 省记录.id, level: 2 } });
        if (!市记录) {
          console.log(`⚠️  未找到城市：${省名} > ${市名}，跳过`);
          未找到++;
          continue;
        }

        for (const [区名, 街道列表] of Object.entries(市数据)) {
          // 查找区级记录
          const 区记录 = await Region.findOne({ where: { name: 区名, parent_id: 市记录.id, level: 3 } });
          if (!区记录) {
            未找到++;
            continue;
          }

          // 检查是否已有街道数据
          const 已有数量 = await Region.count({ where: { parent_id: 区记录.id, level: 4 } });
          if (已有数量 > 0) {
            跳过++;
            continue;
          }

          // 批量插入街道
          const 批量数据 = 街道列表.map((街道名, 索引) => ({
            name: 街道名,
            parent_id: 区记录.id,
            level: 4,
            is_enabled: 1,
            sort: 索引 + 1,
          }));
          await Region.bulkCreate(批量数据);
          总计 += 街道列表.length;
          console.log(`✅ ${省名} > ${市名} > ${区名}：插入 ${街道列表.length} 条街道/乡镇`);
        }
      }
    }

    console.log(`\n✅ 街道数据初始化完成！`);
    console.log(`   插入：${总计} 条`);
    console.log(`   跳过（已有数据）：${跳过} 个区县`);
    console.log(`   未找到（DB中无对应区县）：${未找到} 个`);
    process.exit(0);
  } catch (错误) {
    console.error('初始化街道数据出错:', 错误);
    process.exit(1);
  }
}

初始化街道数据();
